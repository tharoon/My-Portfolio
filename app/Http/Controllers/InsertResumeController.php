    public function insertResume(Request $request){
        $resume = new resume();

        $resume->company_name = $request->get('companyname');
        $resume->designation = $request->get('designation');
        $resume->from_date = $request->get('fromdate');
        $resume->to_date = $request->get('todate');
        $resume->box = $request->get('box');
        $resume->save();

        return redirect('admin');
    }


